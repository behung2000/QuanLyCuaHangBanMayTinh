    private void jBSapXepNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSapXepNVActionPerformed
        // TODO add your handling code here:
        int a=tableNV.getRowCount();
        int i=0;
        ArrayList<NhanVien> ListSapXep = new ArrayList<NhanVien>();
        while(i<a)
        {
            NhanVien nv = new NhanVien();
            nv.setMaNV(tableNV.getModel().getValueAt(i,0).toString());
            nv.setTenNV(tableNV.getModel().getValueAt(i,1).toString());
            nv.setCMND(tableNV.getModel().getValueAt(i,2).toString());
            nv.setPassword(tableNV.getModel().getValueAt(i,3).toString());
            String cv=tableNV.getModel().getValueAt(i,4).toString();
            int j=0;
            if(cv.equals("Quản lý cửa hàng")) j=1;
            else
            {
                if(cv.equals("Nhân Viên bình thường")) j=2;
                else if(cv.equals("Không có Nhân Viên này")) j=3;
            }
            nv.setChucvu(j);
            nv.setluong(tableNV.getModel().getValueAt(i,5).hashCode());
            ListSapXep.add(nv);
            i++;
        }
        RunNhanVien r = new RunNhanVien();
        i=-1;
        if(jRBTheoMaNV.isSelected()) i=0;
        else
        {
            if(jRBTheoTenNV.isSelected()) i=1;
            else
            {
                if(jRBTheoCMNDNV.isSelected()) i=2;
                else
                {
                    if(jRBTheoChucVuNV.isSelected()) i=3;
                }
            }
        }
        tableNV.setModel(r.SapXep(ListSapXep,i));
    }//GEN-LAST:event_jBSapXepNVActionPerformed

