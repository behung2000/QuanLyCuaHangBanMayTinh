    private void jBBanHangThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBanHangThanhToanActionPerformed
        // TODO add your handling code here:
        resetdatabase();
        RunHoaDon hd = new RunHoaDon();
        String MaHD = hd.CreateHoaDon();
        RunKhachHang kh = new RunKhachHang();
        String cmnd = jTFBanHangCMNDKH.getText();
        KhachHang NgMua = new KhachHang();
        NgMua = kh.timkhachhang(cmnd);
        if(NgMua==null)
        {
            String Ten = jTFBanHangTenKH.getText();
            String SDT = jTFBanHangSDTKH.getText();
            String DiaChi = jTFBanHangDiaChiKH.getText();
            kh.AddKH(cmnd, Ten, SDT, DiaChi);
            resetdatabase();
            NgMua = kh.timkhachhang(cmnd);
        }
        if(NgMua!=null)
        {
            RunNhanVien nv = new RunNhanVien();
            NhanVien NgBan = new NhanVien();
            NgBan = nv.timkiem1NV(ArrayListALL.MaNV);
            Date today = new Date();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String ngay = df.format(today);
            int Tong = Integer.parseInt(jLTongBanHang.getText());
            hd.AddHD(MaHD, NgBan.getMaNV(), NgMua.getCMND(), ngay, Tong);
            resetdatabase();
            HoaDon PhieuBan = new HoaDon();
            PhieuBan = hd.XuatHD(MaHD);
            int n = jTableBanHang.getRowCount();
            ArrayList<TTSanPham> GioHang = new ArrayList<TTSanPham>();
            RunSanPham sp = new RunSanPham();
            RunCTHoaDon cthd = new RunCTHoaDon();
            for (int i = 0; i < n; i++) {
                String MaSP = jTableBanHang.getModel().getValueAt(i, 0).toString();
                String MaKM = jTableBanHang.getModel().getValueAt(i, 1).toString();
                int soluongmua = Integer.parseInt(jTableBanHang.getModel().getValueAt(i, 2).toString());
                int Tien = Integer.parseInt(jTableBanHang.getModel().getValueAt(i, 3).toString());
                TTSanPham HangMua = new TTSanPham();
                HangMua = sp.xuatSP(MaSP);
                if (HangMua != null) {
                    cthd.AddCTHD(MaHD, MaSP, MaKM, soluongmua, Tien);
                    cthd.TruSP(MaSP, (HangMua.getSL() - soluongmua));
                    GioHang.add(HangMua);
                }
            }
            resetdatabase();
            XuatPhieu XPhieu = new XuatPhieu();
            XPhieu.XuatHD(PhieuBan, NgBan, NgMua);
            resetdatabase();
        }
        resetdatabase();
    }//GEN-LAST:event_jBBanHangThanhToanActionPerformed

