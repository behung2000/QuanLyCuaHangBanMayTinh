    private void jTableSPBanHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSPBanHangMouseClicked
        // TODO add your handling code here:
        int sel = jTableSPBanHang.getSelectedRow();
        if(sel != -1)
        {
            String masp = jTableSPBanHang.getModel().getValueAt(sel,0).toString();
            int soluong = Integer.parseInt(jTableSPBanHang.getModel().getValueAt(sel,4).toString());
            int tien = Integer.parseInt(jTableSPBanHang.getModel().getValueAt(sel,5).toString());
            int soluongmua = 0;
            boolean dk=true;
            int dem=0;
            while(soluongmua<=0 && dk==true)
            {
                if(dem!=0)
                {
                    JOptionPane.showInternalMessageDialog(null,"Nhập sai vui lòng nhập lại");
                    dem=0;
                }
                String s = JOptionPane.showInputDialog("Nhập số lượng cần mua");
                if(s==null) dk=false;
                try
                {
                    soluongmua = Integer.parseInt(s);
                    dk=false;
                    if(soluongmua>soluong)
                    {
                        JOptionPane.showInternalMessageDialog(null,"không đủ hàng xin nhập lại");
                        soluongmua=0;
                        dk=true;
                    }
                }
                catch(NumberFormatException exception)
                {
                    dem++;
                }
            }
            if(dk==false)
            {
                RunCTHoaDon cthd = new RunCTHoaDon();
                DefaultTableModel modelbanhang = new DefaultTableModel();
                modelbanhang = (DefaultTableModel) jTableBanHang.getModel();
                modelbanhang.addRow(cthd.BanHang(masp, soluongmua, tien));
                jTableBanHang.setModel(modelbanhang);
                int selection = jTableBanHang.getRowCount();
                int tong = Integer.parseInt(jLTongBanHang.getText());
                tong = tong + Integer.parseInt(jTableBanHang.getModel().getValueAt(selection-1,3).toString());
                String thanhtien=""+tong;
                jLTongBanHang.setText(thanhtien);
            }
        }
        else JOptionPane.showInternalMessageDialog(null,"không thấy đồi tượng (bạn chưa chọn)");
    }//GEN-LAST:event_jTableSPBanHangMouseClicked

