    private void jBNhapHangOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBNhapHangOkActionPerformed
        // TODO add your handling code here:
        resetdatabase();
        RunPhieuNhap pn = new RunPhieuNhap();
        String mapn = pn.CreatePhieuNhap();
        RunCungCap cc = new RunCungCap();
        String macc = jCBNhapHangNcc.getSelectedItem().toString();
        NhaCC ncc = new NhaCC();
        ncc = cc.XuatNhaCC(macc);
        if(ncc != null)
        {
            RunNhanVien nv = new RunNhanVien();
            NhanVien NgNhap = new NhanVien();
            NgNhap = nv.timkiem1NV(ArrayListALL.MaNV);
            Date today = new Date();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String ngay = df.format(today);
            int Tong = Integer.parseInt(jLNhapHangTong.getText());
            pn.AddHD(mapn, NgNhap.getMaNV(), macc, ngay, Tong);
            resetdatabase();
            PhieuNhap PNHang = new PhieuNhap();
            PNHang = pn.XuatPN(mapn);
            int n = jTNhapHangPhieuNhap.getRowCount();
            ArrayList<TTSanPham> GioHang = new ArrayList<TTSanPham>();
            RunSanPham sp = new RunSanPham();
            RunCTPhieuNhap ctpn = new RunCTPhieuNhap();
            for(int i =0 ; i<n ; i++)
            {
                String MaSP = jTNhapHangPhieuNhap.getModel().getValueAt(i, 0).toString();
                int SoL = Integer.parseInt(jTNhapHangPhieuNhap.getModel().getValueAt(i, 1).toString());
                int Tien = Integer.parseInt(jTNhapHangPhieuNhap.getModel().getValueAt(i, 2).toString());
                TTSanPham HangNhap = new TTSanPham();
                HangNhap = sp.xuatSP(MaSP);
                if (HangNhap != null) {
                    ctpn.AddCTPN(mapn, MaSP, SoL, Tien);
                    ctpn.CongSP(MaSP, HangNhap.getSL(), SoL, Tien);
                    GioHang.add(HangNhap);
                }
            }
            resetdatabase();
            XuatPhieu XPhieu = new XuatPhieu();
            XPhieu.XuatPN(PNHang, NgNhap, ncc);
            resetdatabase();
        }
        resetdatabase();
    }//GEN-LAST:event_jBNhapHangOkActionPerformed


    
