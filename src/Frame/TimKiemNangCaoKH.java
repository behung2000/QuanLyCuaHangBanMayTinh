    public ArrayList TraGiaTri()
    {
        int n = TimKiemNCKH.getRowCount();
        ArrayList<KhachHang> KH = new ArrayList<KhachHang>();
        for(int i=0; i<n; i++)
        {
            KhachHang kh = new KhachHang();
            kh.setCMND(TimKiemNCKH.getModel().getValueAt(i, 0).toString());
            kh.setTenKH(TimKiemNCKH.getModel().getValueAt(i, 1).toString());
            kh.setSDT(TimKiemNCKH.getModel().getValueAt(i, 2).toString());
            kh.setDiaChi(TimKiemNCKH.getModel().getValueAt(i, 3).toString());
            KH.add(kh);
        }
        return KH;
    }
    
    public void TraVeHT0()
    {
        ArrayList<KhachHang> KH = TraGiaTri();
        DefaultTableModel modeltable = null;
        FramesTable FT = new FramesTable();
        modeltable = FT.khungtableKH(KH);
        Menu.modelTKNC= modeltable;
        Menu.XuatGTTimKiemNCKH();
    }
    
    public void TraVeHT1()
    {
        int i = TimKiemNCKH.getSelectedRow();
        if(i!=-1)
        {
            String ma = TimKiemNCKH.getModel().getValueAt(i,0).toString();
            String ten = TimKiemNCKH.getModel().getValueAt(i,1).toString();
            String sdt = TimKiemNCKH.getModel().getValueAt(i,2).toString();
            String diachi = TimKiemNCKH.getModel().getValueAt(i,3).toString();
            Menu.xuatdulieuKH(ma, ten, sdt, diachi);
        }
        else JOptionPane.showInternalMessageDialog(null,"Chưa chọn đối tượng !");
    }
    
