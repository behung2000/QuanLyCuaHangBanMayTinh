    public void XuatBangTimKiem(DefaultTableModel modeltable)
    {
        TimKiemNCKH.setModel(modeltable);
        if(modeltable.getRowCount()==0) JOptionPane.showInternalMessageDialog(null,"Kết quả tìm kiếm không có !");
    }
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String CM ="";
        CM = CMND.getText();
        String Ten ="";
        Ten = TenKH.getText();
        String SoDT ="";
        SoDT = SDT.getText();
        DefaultTableModel modeltable = null;
        if(AND.isSelected())
        {
            modeltable=TK.TK_And(CM, Ten, SoDT);
            XuatBangTimKiem(modeltable);
        }
        else
        {
            if(OR.isSelected()) 
            {
                modeltable=TK.TK_Or(CM, Ten, SoDT);
                XuatBangTimKiem(modeltable);
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

