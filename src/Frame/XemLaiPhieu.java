    public void luachon(int i)
    {
        switch(i)
        {
            case 0: xuatHoaDon();break;
            case 1: xuatPhieuNhap();break;
            default: break;
        }
    }
    
    public void xuatHoaDon()
    {
        RunHoaDon hd = new RunHoaDon();
        jTableHoaDon.setModel(hd.ReadHD());
        jTableCTHoaDon.setModel(FT.khungtableCTHDonBanHang());
    }
    
    public void xuatPhieuNhap()
    {
        RunPhieuNhap pn = new RunPhieuNhap();
        jTablePhieuNhap.setModel(pn.ReadPN());
        jTableCTPhieuNhap.setModel(FT.khungtableCTPhieuNhap());
    }
    
