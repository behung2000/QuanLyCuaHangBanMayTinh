    private void jTableHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableHoaDonMouseClicked
        // TODO add your handling code here:
        int sel = jTableHoaDon.getSelectedRow();
        if(sel!=-1)
        {
            String mahd = jTableHoaDon.getModel().getValueAt(sel,0).toString();
            jLHoaDonMaHD.setText(mahd);
            String manv = jTableHoaDon.getModel().getValueAt(sel,1).toString();
            String makh = jTableHoaDon.getModel().getValueAt(sel,2).toString();
            String Ngay = jTableHoaDon.getModel().getValueAt(sel,3).toString();
            jLHoaDonNgay.setText(Ngay);
            RunNhanVien rnv = new RunNhanVien();
            NhanVien nv = new NhanVien();
            nv=rnv.timkiem1NV(manv);
            jLHoaDonMaNV.setText(nv.getMaNV());
            jLHoaDonTenNV.setText(nv.getTenNV());
            RunKhachHang rkh = new RunKhachHang();
            KhachHang kh = new KhachHang();
            kh = rkh.timkhachhang(makh);
            jLHoaDonMaKH.setText(kh.getCMND());
            jLHoaDonTenKH.setText(kh.getTenKH());
            jTableCTHoaDon.removeAll();
            jTableCTHoaDon.setModel(FT.khungtableCTHDonBanHang());
            for(CTHoaDon cthd : ArrayListALL.ListCTHD)
            {
                if(mahd.equals(cthd.getMaP()))
                {
                    DefaultTableModel model = new DefaultTableModel();
                    model = (DefaultTableModel) jTableCTHoaDon.getModel();
                    Vector row = new Vector();
                    row.add(cthd.getMaSP());
                    row.add(cthd.getMaKM());
                    row.add(cthd.getSL());
                    row.add(cthd.getTTien());
                    model.addRow(row);
                    jTableCTHoaDon.setModel(model);
                }
            }
        }
    }//GEN-LAST:event_jTableHoaDonMouseClicked

